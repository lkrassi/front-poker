<div class="rules-container">
	<h1 class="rules-container__title">Правила Техасского Холдема</h1>

	<h2>1. Общая информация</h2>
	<p>
		Техасский Холдем — это одна из самых популярных разновидностей покера. В игре используется
		стандартная колода из 52 карт. Цель игры — собрать лучшую комбинацию из пяти карт или вынудить
		всех соперников сбросить карты.
	</p>

	<h2>2. Ход игры</h2>
	<ul>
		<li>
			Игра начинается с того, что два игрока слева от дилера делают обязательные ставки (блайнды).
		</li>
		<li>Каждому игроку раздается по две карты в закрытую (карманные карты).</li>
		<li>Затем происходит первый раунд торговли (префлоп).</li>
		<li>
			На стол выкладываются три общие карты (флоп), за которыми следует второй раунд торговли.
		</li>
		<li>Добавляется четвертая общая карта (терн), и проходит третий раунд торговли.</li>
		<li>Выкладывается пятая общая карта (ривер), после чего проходит последний раунд торговли.</li>
		<li>Если осталось два или более игроков, происходит вскрытие карт (шоудаун).</li>
	</ul>

	<h2>3. Комбинации карт</h2>
	<p>Комбинации ранжируются от старшей к младшей:</p>
	<ul>
		<li><strong>Роял-флэш</strong>: Старшая последовательность от 10 до туза одной масти.</li>
		<li><strong>Стрит-флэш</strong>: Любая последовательность из пяти карт одной масти.</li>
		<li><strong>Каре</strong>: Четыре карты одного достоинства.</li>
		<li><strong>Фулл-хаус</strong>: Три карты одного достоинства и две другого.</li>
		<li><strong>Флэш</strong>: Пять карт одной масти.</li>
		<li><strong>Стрит</strong>: Пять последовательных карт разной масти.</li>
		<li><strong>Тройка</strong>: Три карты одного достоинства.</li>
		<li><strong>Две пары</strong>: Две пары карт одного достоинства.</li>
		<li><strong>Пара</strong>: Две карты одного достоинства.</li>
		<li>
			<strong>Старшая карта</strong>: Если ни у кого нет комбинации, побеждает игрок с самой старшей
			картой.
		</li>
	</ul>

	<h2>4. Торговля</h2>
	<p>В каждом раунде торговли игроки могут:</p>
	<ul>
		<li><strong>Колл</strong>: Сделать ставку, равную предыдущей.</li>
		<li><strong>Рейз</strong>: Увеличить ставку.</li>
		<li><strong>Фолд</strong>: Сбросить карты и выйти из текущей раздачи.</li>
		<li><strong>Чек</strong>: Пропустить ход, если не требуется делать ставку.</li>
	</ul>

	<h2>5. Шоудаун</h2>
	<p>
		Если после последнего раунда торговли осталось два или более игроков, происходит вскрытие карт.
		Игроки используют свои две карманные карты и пять общих карт на столе, чтобы собрать лучшую
		комбинацию из пяти карт. Игрок с лучшей комбинацией выигрывает банк.
	</p>

	<h2>6. Дополнительные правила</h2>
	<ul>
		<li>Если у двух игроков одинаковые комбинации, банк делится поровну.</li>
		<li>Дилерская кнопка перемещается по часовой стрелке после каждой раздачи.</li>
		<li>
			Игроки могут договориться о лимитах ставок (фиксированный лимит, безлимитный или пот-лимит).
		</li>
	</ul>
</div>

